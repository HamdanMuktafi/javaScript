// Increament & Decreament
/*
Operatorincrementadalah sebutan untuk penulisan seperti i++. Perintah ini digunakan untuk menaikkan nilai variabel i sebanyak 1 angka. Perintah i++ adalah penulisan singkat dari i = i + 1. Sedangkan operator decrement adalah sebutan untuk penulisan seperti i--. Fungsinya untuk menurunkan nilai variabel i sebesar 1 angka. Perintah i-- merupakan penulisan singkat dari i = i - 1. 

* pre-increment ++a tambah nilai a dengan 1, lalu kirim nilai a 
* post-increment a++ kirim nilai a, lalu tambah nilai a dengan 1 
* pre-decrement --a kurangi nilai a dengan 1, lalu kirim nilai a 
*post-decrement a-- kirim nilai a, lalu kurangi nilai a dengan 1

*/

var a =  5 ;
console.log(++a);
console.log(a);

var b = 6;
console.log(b++);
console.log(b);

var c = 8;
console.log(--c);
console.log(c);

var d = 7;
console.log(c--);
console.log(c);
